package org.ischool.mod;

import java.util.List;


public class ResultIfo
{
	String subject;
	String point;
	String result;
	String sClass;
	String sCode;
	String insSubject;
	String insSCode;
	String insSPoint;
	String insSResult;
	
	public ResultIfo()
	{
		
	}
	
	public ResultIfo(List<String> list)
	{
		this.subject = list.get(0);
		this.point = list.get(1);
		this.result = list.get(2);
		this.sClass = list.get(3);
		this.sCode = list.get(4);
		this.insSubject = list.get(5);
		this.insSCode = list.get(6);
		this.insSPoint = list.get(7);
		this.insSResult = list.get(8);
	}
	
	public void SetSubject(String value)
	{
		this.subject = value;
	}
	
	public void SetPoint(String value)
	{
		this.point = value;
	}
	
	public void SetResult(String value)
	{
		this.result = value;
	}
	
	public void SetSClass(String value)
	{
		this.sClass = value;
		
	}
	
	public void SetSCode(String value)
	{
		this.sCode = value;
	}

	public void SetInsSubject(String value)
	{
		this.insSubject = value;
	}
	
	public void SetInsSCode(String value)
	{
		this.insSCode = value;
	}
	public void SetInsSPoint(String value) 
	{
		this.insSPoint = value;
	}
	public void SetInsSResult(String value)
	{
		this.insSResult = value;
	}
	
	public String GetSubject()
	{
		return this.subject;
	}
	
	public String GetPoint()
	{
		return this.point;
	}
	
	public String GetResult()
	{
		return this.result;
	}

	public String GetSClass()
	{
		return this.sClass;
	}
	
	public String GetSCode()
	{
		return this.sCode;
	}
	
	public String GetInsSubject()
	{
		return this.insSubject;
	}
	
	public String GetInsCode()
	{
		return this.insSCode;
	}
	
	public String GetInsResult()
	{
		return this.insSResult;
	}
	
	public String GetInsPoint()
	{
		return this.insSPoint;
	}
}
